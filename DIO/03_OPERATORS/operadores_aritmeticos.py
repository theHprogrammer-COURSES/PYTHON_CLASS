produto_1 = 10
produto_2 = 20

# Adição
soma = produto_1 + produto_2
print(soma)

# Subtração
subtracao = produto_1 - produto_2
print(subtracao)

# Multiplicação
multiplicacao = produto_1 * produto_2

# Divisão
divisao = produto_1 / produto_2
print(divisao)

# Divisão inteira
divisao_inteira = produto_1 // produto_2
print(divisao_inteira)

# Resto da divisão
resto_divisao = produto_1 % produto_2
print(resto_divisao)

# Exponenciação
exponenciacao = produto_1 ** produto_2
print(exponenciacao)

# Prioridade de operadores
resultado = 10 + 20 * 30 ** 2
print(resultado)

resultado = (10 + 20) * 30 ** 2
print(resultado)
