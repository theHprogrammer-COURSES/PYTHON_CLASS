saldo = 200
saque = 200

print(saldo == saque)  # True
print(saldo != saque)  # False
print(saldo > saque)  # False
print(saldo < saque)  # False
print(saldo >= saque)  # True
print(saldo <= saque)  # True
print(saldo is saque)  # True
print(saldo is not saque)  # False
print(saldo in [100, 200, 300])  # True
print(saque not in [100, 200, 300])  # False
