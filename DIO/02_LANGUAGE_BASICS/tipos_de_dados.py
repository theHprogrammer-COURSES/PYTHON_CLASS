# inteiro
inteiro = 10
print(f'{inteiro} é do tipo {type(inteiro)}')

# float
flutuante = 10.5
print(f'{flutuante} é do tipo {type(flutuante)}')

# string
texto = "Olá, mundo!"
print(f'{texto} é do tipo {type(texto)}')

# boolean
booleano = True
print(f'{booleano} é do tipo {type(booleano)}')

# complex
complexo = 1 + 2j
print(f'{complexo} é do tipo {type(complexo)}')

# list
lista = [1, 2, 3, 4, 5]
print(f'{lista} é do tipo {type(lista)}')

# tuple
tupla = (1, 2, 3, 4, 5)
print(f'{tupla} é do tipo {type(tupla)}')

# set
conjunto = {1, 2, 3, 4, 5}
print(f'{conjunto} é do tipo {type(conjunto)}')

# dict
dicionario = {'nome': 'João', 'idade': 25}
print(f'{dicionario} é do tipo {type(dicionario)}')

# None
nulo = None
print(f'{nulo} é do tipo {type(nulo)}')

# bytes
bytes = b'Python'
print(f'{bytes} é do tipo {type(bytes)}')

# bytearray
bytearray = bytearray(10)
print(f'{bytearray} é do tipo {type(bytearray)}')

# memoryview
memoria = memoryview(bytes)
print(f'{memoria} é do tipo {type(memoria)}')

# range
intervalo = range(10)
print(f'{intervalo} é do tipo {type(intervalo)}')

# frozenset
conjunto_imutavel = frozenset({1, 2, 3, 4, 5})
print(f'{conjunto_imutavel} é do tipo {type(conjunto_imutavel)}')
